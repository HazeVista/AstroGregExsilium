StartupEvents.registry('block', event => {

    event.create('astrogreg:livingbricks')
        .translationKey('block.astrogreg.livingbricks')
        .hardness(0.8)
        .resistance(0.8)
        .soundType('stone')
        .requiresTool(true)
        .mapColor('terracotta_light_gray')
        .tagBlock('mineable/pickaxe')
        .textureAll('kubejs:block/bricks/livingbricks')
		.fullBlock(true)
        .opaque(true)

    event.create('botania:managlass')
        .translationKey('block.astrogreg.managlass')
		.hardness(5)
		.resistance(2)
		.soundType('glass')
		.tagBlock('gtceu:mineable/pickaxe_or_wrench')
        .tagBoth('c:glass_blocks')
		.mapColor('color_light_blue')
        .textureAll('kubejs:block/glass/managlass')
		.defaultTranslucent()
    
    event.create('botania:alfglass')
        .translationKey('block.astrogreg.alfglass')
		.hardness(5)
		.resistance(2)
		.soundType('glass')
		.tagBlock('gtceu:mineable/pickaxe_or_wrench')
        .tagBoth('c:glass_blocks')
		.mapColor('color_light_blue')
        .textureAll('kubejs:block/glass/alfglass')
		.defaultTranslucent()
})